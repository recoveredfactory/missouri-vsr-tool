#!/usr/bin/env node
import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(HERE, "../../web/content/about-the-data.en.md");
const OUT = resolve(HERE, "../src/content/about-the-data.gen.ts");

const STRIP_BLOCKS = [
  { start: "<!-- AGENCY_SLUGS_START -->", end: "<!-- AGENCY_SLUGS_END -->" },
  { start: "<!-- METRIC_KEYS_START -->", end: "<!-- METRIC_KEYS_END -->" },
];

const STRIP_REPLACEMENT =
  "_(Per-agency / per-metric URL listings omitted — call `list_agencies()` or `read_schema()` instead.)_";

function stripMarkered(md) {
  let out = md;
  for (const { start, end } of STRIP_BLOCKS) {
    const startIdx = out.indexOf(start);
    const endIdx = out.indexOf(end);
    if (startIdx === -1 || endIdx === -1 || endIdx < startIdx) {
      throw new Error(`Missing or out-of-order markers: ${start} / ${end}`);
    }
    out =
      out.slice(0, startIdx) +
      STRIP_REPLACEMENT +
      out.slice(endIdx + end.length);
  }
  return out;
}

const md = await readFile(SRC, "utf8");
const stripped = stripMarkered(md);

const banner = `// AUTO-GENERATED by packages/mcp/scripts/sync-about-data.mjs — do not edit.
// Source: packages/web/content/about-the-data.en.md
// Run \`pnpm -F @missouri-vsr-tool/mcp build\` to regenerate.

`;

const body = `export const ABOUT_THE_DATA_MD = ${JSON.stringify(stripped)};\n`;

await mkdir(dirname(OUT), { recursive: true });
await writeFile(OUT, banner + body, "utf8");

const beforeBytes = Buffer.byteLength(md, "utf8");
const afterBytes = Buffer.byteLength(stripped, "utf8");
console.log(
  `sync-about-data: ${SRC} (${beforeBytes}B) → ${OUT} (${afterBytes}B narrative)`,
);
